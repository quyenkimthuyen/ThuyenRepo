function audioName(text) {
  return `${text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "")}.mp3`;
}

function sentence(english, meaning) {
  return { english, meaning, audio: audioName(english) };
}

function phrase(phraseText, meaning) {
  return { phrase: phraseText, meaning, audio: audioName(phraseText) };
}

function vocab(word, ipa, meaning, emoji) {
  return { word, ipa, meaning, emoji, audio: audioName(word) };
}

function game(type, prompt, answer) {
  return answer ? { type, prompt, answer } : { type, prompt };
}

