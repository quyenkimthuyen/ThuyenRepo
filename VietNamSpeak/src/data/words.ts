export function getWordById(id: string) {
  return WORDS.find((word) => word.id === id);
}

export function getNextWordId(id: string) {
  const index = WORDS.findIndex((item) => item.id === id);
  return WORDS[(index + 1) % WORDS.length]?.id ?? WORDS[0].id;
}

export function getTopics() {
  return Array.from(new Set(WORDS.map((item) => item.topic)));
}

